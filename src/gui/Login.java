    public void verificar(Gerente g, boolean verdadeiro) throws Exception {

        if (verdadeiro == false) {
            jTextFieldLogin.setText("");
            jTextFieldLogin.requestFocus();
            jPasswordFieldSenha.setText("");
        }
        fachada.autenticar(g, verdadeiro);
    }

    private void jButtonLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonLoginActionPerformed

        String login = jTextFieldLogin.getText();
        String senha = new String(jPasswordFieldSenha.getPassword());

        boolean verdadeiro = false;
        
        try {

            if (login.equals("")) {
                JOptionPane.showMessageDialog(rootPane, "O campo login esta vazio!", "Aviso", JOptionPane.WARNING_MESSAGE);
                jTextFieldLogin.requestFocus();
                return;
            } else if (login.length() <= 2) {
                JOptionPane.showMessageDialog(rootPane, "O login nao pode ser menor que 2 (DOIS) caracteres! Informe um login valido.", "Aviso", JOptionPane.WARNING_MESSAGE);
                jTextFieldLogin.requestFocus();
                return;
            } else if (senha.equals("")) {
                JOptionPane.showMessageDialog(rootPane, "O campo senha esta vazio!", "Aviso", JOptionPane.WARNING_MESSAGE);
                jPasswordFieldSenha.requestFocus();
                return;
            } else if (senha.length() <= 2) {
                JOptionPane.showMessageDialog(rootPane, "O senha nao pode ser menor que 2 (DOIS) caracteres! Informe um senha valido.", "Aviso", JOptionPane.WARNING_MESSAGE);
                jPasswordFieldSenha.requestFocus();
                return;
            }

            gerente.setLogin(login);
            gerente.setSenha(senha);

            verificar(gerente, verdadeiro);
            
            Principal principal = new Principal();
            principal.setVisible(true);
            Login.this.dispose();

        } catch (Exception ex) {
            ex.getMessage();
        }

    }//GEN-LAST:event_jButtonLoginActionPerformed

